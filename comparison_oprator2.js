let a = 5;
let b = "5";
let c = 10;

console.log("==  :", a == b);    // true
console.log("=== :", a === b);   // false
console.log("!=  :", a != c);    // true
console.log("!== :", a !== b);   // true
console.log(">   :", c > a);     // true
console.log("<   :", a < c);     // true
console.log(">=  :", c >= 10);   // true
console.log("<=  :", a <= c);    // true